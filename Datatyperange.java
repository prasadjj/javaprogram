package com.edu;

public class Datatyperange {

	public static void main(String[] args) {
		// Range of datatype
		//integer
		int i=20000;
		System.out.println(i);
		
		//double
		double d=99.99;
		System.out.println(d);
		
		//Boolean
		boolean b1=true;
		System.out.println(b1);
		
		//byte
		byte b=127;
		System.out.println(b);
		
		//float
		float f=9.99f;
		System.out.println(f);
		
		//character
		char c='B';
		System.out.println(c);
		

	}

}
