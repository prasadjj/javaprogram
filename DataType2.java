package com.edu;

public class DataType2 {

	public static void main(String[] args) {
		// //decalaration of variables
		int intval;
		float fval; 
		double dval;
		char cval; 
		short sval;
		byte bval;
		long lval; 
		boolean boolval;
		//Assign the values
		intval =987;
		fval=567.3f;
		dval=987.4; 
		cval='A';
		lval=987l;
		boolval=true; 
		sval=87;
		bval=65; 
		//Displaying 
		System.out.println("Integer value="+intval);
		System.out.println("float value="+fval);
		System.out.println("double val="+dval); 
		System.out.println("short val="+sval);
		System.out.println("Byte val="+bval);
		System.out.println("long val="+lval);
		System.out.println("Boolean val="+boolval); 
		System.out.println("Character value="+cval); 

	}

}
